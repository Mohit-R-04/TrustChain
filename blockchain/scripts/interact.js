import hre from "hardhat";
import fs from "fs";
import path from "path";

/**
 * Contract Interaction Script
 * Demonstrates complete TrustChainEscrow workflow
 */

// Load latest deployment
function loadDeployment() {
  const latestPath = path.join("./deployments", "latest.json");
  if (!fs.existsSync(latestPath)) {
    throw new Error("No deployment found. Run 'npm run deploy' first");
  }
  return JSON.parse(fs.readFileSync(latestPath, "utf-8"));
}

async function main() {
  console.log("üîß TrustChainEscrow Contract Interaction\n");

  const deployment = loadDeployment();
  const { contractAddress, abi } = deployment;

  const [deployer, addr1, addr2] = await hre.ethers.getSigners();
  const contract = new hre.ethers.Contract(contractAddress, abi, deployer);

  console.log(`‚úÖ Connected to contract: ${contractAddress}`);
  console.log(`üë§ Deployer: ${deployer.address}\n`);

  try {
    // Test 1: Create Scheme
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 1Ô∏è‚É£: Create Scheme");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    const schemeId = 1;
    let tx = await contract.createScheme(schemeId);
    await tx.wait();
    console.log(`‚úÖ Scheme created: ID=${schemeId}\n`);

    // Test 2: Deposit Funds
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 2Ô∏è‚É£: Deposit Funds");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    const depositAmount = hre.ethers.parseEther("1"); // 1 POL
    tx = await contract.depositFunds(schemeId, { value: depositAmount });
    await tx.wait();
    console.log(`‚úÖ Funds deposited: ${hre.ethers.formatEther(depositAmount)} POL`);

    // Verify balance
    const balance = await contract.getSchemeBalance(schemeId);
    console.log(`üí∞ Scheme balance: ${hre.ethers.formatEther(balance)} POL\n`);

    // Test 3: Create Milestone
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 3Ô∏è‚É£: Create Milestone");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    const milestoneId = 1;
    const milestoneAmount = hre.ethers.parseEther("0.5");
    tx = await contract.createMilestone(schemeId, milestoneId, milestoneAmount);
    await tx.wait();
    console.log(`‚úÖ Milestone created: ID=${milestoneId}, Amount=${hre.ethers.formatEther(milestoneAmount)} POL`);

    // Get milestone details
    const milestone = await contract.getMilestone(schemeId, milestoneId);
    console.log(`   Status: ${milestone.status} (Created=1)\n`);

    // Test 4: Lock Funds
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 4Ô∏è‚É£: Lock Funds");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    tx = await contract.lockFunds(schemeId);
    await tx.wait();
    console.log(`‚úÖ Funds locked for scheme ${schemeId}\n`);

    // Test 5: Set Vendor
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 5Ô∏è‚É£: Assign Vendor");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    const vendorAddress = addr1.address;
    tx = await contract.setVendorForMilestone(schemeId, milestoneId, vendorAddress);
    await tx.wait();
    console.log(`‚úÖ Vendor assigned: ${vendorAddress}\n`);

    // Test 6: Store Quotation Hash
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 6Ô∏è‚É£: Store Quotation (IPFS Hash)");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    const quotationHash = "QmExample123quotation456hash789";
    tx = await contract.storeQuotationHash(schemeId, milestoneId, quotationHash);
    await tx.wait();
    console.log(`‚úÖ Quotation stored: ${quotationHash}\n`);

    // Test 7: Submit Proof (Vendor)
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 7Ô∏è‚É£: Submit Proof (Vendor Action)");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    const proofHash = "QmExample456proof789hash123";
    const contractAsVendor = new hre.ethers.Contract(contractAddress, abi, addr1);
    tx = await contractAsVendor.submitProof(schemeId, milestoneId, proofHash);
    await tx.wait();
    console.log(`‚úÖ Proof submitted by vendor: ${proofHash}`);

    // Get proof details
    const proof = await contract.getProof(schemeId, milestoneId);
    console.log(`   Vendor: ${proof.vendor}`);
    console.log(`   IPFS Hash: ${proof.ipfsHash}\n`);

    // Test 8: Approve Proof
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 8Ô∏è‚É£: Approve Proof (NGO/Admin)");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    tx = await contract.approveProof(schemeId, milestoneId);
    await tx.wait();
    console.log(`‚úÖ Proof approved for milestone ${milestoneId}\n`);

    // Test 9: Release Payment
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("Test 9Ô∏è‚É£: Release Payment (Auto)");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    tx = await contract.releasePayment(schemeId, milestoneId);
    await tx.wait();
    console.log(`‚úÖ Payment released to vendor: ${hre.ethers.formatEther(milestoneAmount)} POL`);

    // Get final milestone status
    const finalMilestone = await contract.getMilestone(schemeId, milestoneId);
    console.log(`   Final Status: ${finalMilestone.status} (Released=5)\n`);

    // Final Summary
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("‚úÖ COMPLETE WORKFLOW TEST SUCCESSFUL!");
    console.log("‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê");
    console.log("\nüìä Transaction Flow Summary:");
    console.log("  1. ‚úÖ Scheme Created");
    console.log("  2. ‚úÖ Funds Deposited");
    console.log("  3. ‚úÖ Milestone Created");
    console.log("  4. ‚úÖ Funds Locked");
    console.log("  5. ‚úÖ Vendor Assigned");
    console.log("  6. ‚úÖ Quotation Stored (IPFS)");
    console.log("  7. ‚úÖ Proof Submitted (IPFS)");
    console.log("  8. ‚úÖ Proof Approved");
    console.log("  9. ‚úÖ Payment Released\n");

  } catch (error) {
    console.error("‚ùå Error during interaction:", error.message);
    process.exit(1);
  }
}

